    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if (opcion.equals("nuevo")) {
            if (txtNombre.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "No puede haber campos vacios");
                txtNombre.requestFocus();
            } else {
                try {
//                    cp.getCstock().nuevaMarca(txtNombre.getText());
                    JOptionPane.showMessageDialog(null, "La Marca se registró con éxito ");
                    //                  UtilVista.cargarCombo(cp.getCstock().traerMarcas(), st.cmbMarca);
                    estadoInicial();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, e.getMessage());
                }
            }
        } else if (opcion.equals("modificar")) {
            if (tblMarcaProducto.getSelectedRow() > -1 && tblMarcaProducto.getSelectedRowCount() == 1) {
                int seleccionado = (int) tblMarcaProducto.getValueAt(tblMarcaProducto.getSelectedRow(), 0);
                if (txtNombre.getText().isEmpty()) {
                    JOptionPane.showMessageDialog(null, "No puede haber campos vacios");
                    txtNombre.requestFocus();
                } else {
                    try {
                        //                    cp.getCstock().editarMarca(seleccionado, txtNombre.getText());
                        JOptionPane.showMessageDialog(null, "La Marca se modificó con éxito ");
                        //                  UtilVista.cargarCombo(cp.getCstock().traerMarcas(), st.cmbMarca);
                        estadoInicial();
                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(null, e.getMessage());
                    }

                }
            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

