    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed

        try {
            if (tblMarcaProducto.getSelectedRow() > -1 && tblMarcaProducto.getSelectedRowCount() == 1) {
                int seleccionado = (int) tblMarcaProducto.getValueAt(tblMarcaProducto.getSelectedRow(), 0);
                int seleccion = JOptionPane.showConfirmDialog(rootPane, "¿Esta seguro que desea eliminar esta Marca?", "input", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
                if (seleccion == JOptionPane.YES_OPTION) {

                    //        cp.getCstock().borrarMarca(seleccionado);
                    JOptionPane.showMessageDialog(rootPane, "La Marca se eliminó exitosamente");
                    //          UtilVista.cargarCombo(cp.getCstock().traerMarcas(), st.cmbMarca);
                    estadoInicial();
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "Seleccione un elemento de la Tabla");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnBorrarActionPerformed

