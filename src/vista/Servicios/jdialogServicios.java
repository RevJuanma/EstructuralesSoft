    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if (opcion.equals("nuevo")) {
            if (txtNombre.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "No puede haber campos vacios");
                txtNombre.requestFocus();
            } else {
                try {

                    //                    cp.getCs().nuevoServicio(txtNombre.getText());
                    JOptionPane.showMessageDialog(null, "El Servicio se registró con éxito ");
                    estadoInicial();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, e.getMessage());
                }

            }
        } else if (opcion.equals("modificar")) {
            if (tblServicios.getSelectedRow() > -1 && tblServicios.getSelectedRowCount() == 1) {
                int seleccionado = (int) tblServicios.getValueAt(tblServicios.getSelectedRow(), 0);
                if (txtNombre.getText().isEmpty()) {
                    JOptionPane.showMessageDialog(null, "No puede haber campos vacios");
                    txtNombre.requestFocus();
                } else {
                    try {
                        //                        cp.getCs().editarServicio(seleccionado, txtNombre.getText());
                        JOptionPane.showMessageDialog(null, "El Servicio se modificó con éxito ");
                        estadoInicial();
                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(null, e.getMessage());
                    }

                }
            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

