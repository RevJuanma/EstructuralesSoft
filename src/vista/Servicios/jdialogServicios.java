    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed

        try {
            if (tblServicios.getSelectedRow() > -1 && tblServicios.getSelectedRowCount() == 1) {
                int seleccionado = (int) tblServicios.getValueAt(tblServicios.getSelectedRow(), 0);
                int seleccion = JOptionPane.showConfirmDialog(rootPane, "¿Esta seguro que desea eliminar este Servicio?", "input", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
                if (seleccion == JOptionPane.YES_OPTION) {

                    //                    cp.getCs().borrarServicio(seleccionado);
                    JOptionPane.showMessageDialog(rootPane, "El Servicio se eliminó exitosamente");
                    //                    UtilVista.cargarTablaServicios(cp.getCs().traerServicios(), tblServicios);
                    estadoInicial();
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "Seleccione un elemento de la Tabla");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnBorrarActionPerformed

